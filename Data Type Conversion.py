# conversion of integer
x= 5
print (type(x))
print (f"int to string : {str(x)}")
print (f"int to float : {float(x)}")

# conversion of float
y= 4.5
print (type(y))
print (f"float to integer: {int(y)}")
print (f"float to string:{str(y)}")

# conversion of string
z= "123"
print (z)
print (type(z))
print (f"string to int : {int(z)}")
print (f"string to float : {float(z)}")

# conversion of ilist
my_list=[1,2,3,4,5]
print(type(my_list))
print(f"list to tuple : {tuple(my_list)}")
print(f"list to set : {set(my_list)}")

